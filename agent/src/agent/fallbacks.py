"""兜底话术池：当 LLM 调用失败、需要给用户一个"人话"回复时使用。

设计原则（详见 docs/requirements/001-foundation-chat-and-memory/design.md §4.5 + §4.7.4）：

1. **不暴露技术细节**——不出现"网络 / API / 服务器 / 超时 / 限速"这类内部术语
2. **匹配人设口语化风格**——目前对齐 ``personas/default.md`` 的 Echo 设定
3. **暗示用户重试**——让对话能继续下去
4. **不假装记得发生了什么**——不要说"我们刚才聊到哪了"这种伪记忆
"""

from __future__ import annotations

import random

FALLBACKS: tuple[str, ...] = (
    "嗯……我刚才走神了，能再说一次吗？",
    "诶？我好像没接住，再说一遍呗？",
    "等等，让我重新想想……你刚说的是？",
    "哎呀，我这边有点卡，你再说一次试试？",
    "嗯……我没听清楚，可以再讲一遍吗？",
    "稍等一下哈，我刚才好像没跟上。",
    "抱歉，我有点没回过神，再说一次？",
    "哎，我这会儿状态不太行，能换个方式说吗？",
    "嗯……不好意思，你可以再发一遍嘛？",
    "诶嘿，我没 get 到，重新说说看？",
)


def random_fallback() -> str:
    """返回一条随机兜底话术。"""
    return random.choice(FALLBACKS)
